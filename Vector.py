class Vector:
    def __init__(self, x, y, w, h):
        self.x = x
        self.y = y
        self.w = w
        self.h = h

    def getX():
        return self.x
    def getY():
        return self.y
    def getW():
        return self.w
    def getH():
        return self.h
    def getSize():
        return (self.w, self.h)